    <script>
        function myFunction(mediaQuery) {
            if (mediaQuery.matches) { // If media query matches

                // Less or equal = Do nothing

            } else {

                // Greater than = Execute below code

                const cards = document.querySelectorAll(".interactive-card");

                cards.forEach(function(card){

                    gsap.to(card, {"duration":0,"delay":0,"css":{"background": "#131313"}})

                    card.addEventListener('mouseenter', function(e){
                        var column = e.currentTarget.closest('.interactive-card-column');

                        gsap.to(column.querySelector('.interactive-card'), {"duration":0.25,"delay":0,"css":{"background": "rgba(27,27, 27, 1)"}})
                        gsap.to(column.querySelector('.code-img'), {"duration":0.25,"delay":0,"css":{"y":-16,"opacity":1,"scaleX":1.15,"scaleY":1.15}})
                        gsap.to(column.querySelector('.code-title'), {"duration":0.35,"delay":0,"css":{"opacity":1,"rotationX":0,"y":-16}})
                        gsap.to(column.querySelector('.code-caption'), {"duration":0.35,"delay":0,"css":{"opacity":1,"rotationX":0,"y":-16}})
                    });


                    card.addEventListener('mouseleave', function(e){
                        var column = e.currentTarget.closest('.interactive-card-column');

                        gsap.to(column.querySelector('.interactive-card'), {"duration":0.25,"delay":0,"css":{"background":"#131313"}})
                        gsap.to(column.querySelector('.code-img'), {"duration":0.25,"delay":0,"css":{"y": 0,"opacity":0.2,"scaleX":1,"scaleY":1}})
                        gsap.to(column.querySelector('.code-title'), {"duration":0.35,"delay":0,"css":{"opacity":0,"rotationX":-20,"y":0}})
                        gsap.to(column.querySelector('.code-caption'), {"duration":0.35,"delay":0,"css":{"opacity":0,"rotationX":-20,"y":0}})
                    });
                })

            }
        }

        var mediaQuery = window.matchMedia("(max-width: 768px)")
        myFunction(mediaQuery) // Call listener function at run time
        mediaQuery.addListener(myFunction) // Attach listener function on state changes


    </script>
@endsection

